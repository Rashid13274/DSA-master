/* Find The Difference
Description: Given two s and t consist of only lowercase letters.
String t is generated by random  shuffling string s and then add one more character at a random  position

Find the Letter  that was added to t.

Ex: Input 
s = 'abcd'
t = 'abcde'
Output: e
*/

// Solution 1: Using HashMap
function findDifferenceHashMap(s, t) {
    const charMap = new Map();
    
    // Count characters in string s
    for (let char of s) {
        charMap.set(char, (charMap.get(char) || 0) + 1);
    }
    
    // Check characters in string t
    for (let char of t) {
        if (!charMap.has(char) || charMap.get(char) === 0) {
            return char;
        }
        charMap.set(char, charMap.get(char) - 1);
    }
}

// Solution 2: Using Array (ASCII count)
function findDifferenceArray(s, t) {
    const charCount = new Array(256).fill(0);
    
    // Count characters in string s
    for (let char of s) {
        charCount[char.charCodeAt(0)]++;
    }
    
    // Check characters in string t
    for (let char of t) {
        charCount[char.charCodeAt(0)]--;
        if (charCount[char.charCodeAt(0)] < 0) {
            return char;
        }
    }
}

